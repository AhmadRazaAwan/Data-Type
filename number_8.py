# Python offers modules like math to carry out different
# mathematics like trigonometry, logarithms, probability and statistics, etc.

import math

print("Pi value is :", math.pi)
print("Factorial of 5 is :", math.factorial(5))
print("Cos of 45 is :", math.cos(45))
print("Square root of 4 is :", math.sqrt(4))
print("Anti log of 20 is :", math.log10(20))