# how to access list
# list are in sequence

lst = ["Java", "Python", "C++", "Javascript"]

# for checking list length
print(len(lst))

# Access list by their index (0-3)
print(lst[0])
print(lst[1])
print(lst[2])
print(lst[3])

# Access list by their negative index (-1 , -4)
print(lst[-1])
print(lst[-2])
print(lst[-3])
print(lst[-4])

