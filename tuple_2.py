# how to access tuples
# tuples are in sequence

tpl = ["Java", "Python", "C++", "Javascript"]

# for checking list length
print(len(tpl))

# Access list by their index (0-3)
print(tpl[0])
print(tpl[1])
print(tpl[2])
print(tpl[3])

# Access list by their negative index (-1 , -4)
print(tpl[-1])
print(tpl[-2])
print(tpl[-3])
print(tpl[-4])
