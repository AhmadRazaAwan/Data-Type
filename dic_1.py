# Types of dictionary

# dictionary with integer keys
my_dict = {1: 'apple', 2: 'ball'}
print(my_dict)

# dictionary with mixed keys
my_dict1 = {'name': 'John', 1: [2, 4, 3]}
print(my_dict1)

# using dict() method
my_dict2 = dict({1:'apple', 2:'ball'})
print(my_dict2)

# from sequence having each item as a pair
my_dict3 = dict([(1,'apple'), (2,'ball')])
print(my_dict3)